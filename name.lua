Name.TrancelateName = function(name)

    if Name.DoubleName[string.sub(name,1,4)] then

        -- return Name.DoubleName[string.sub(name,1,4)].." "..string.gsub(string.sub(name,5,10),'(...)',Name.table),2
        return Name.DoubleName[string.sub(name,1,4)].." "..string.gsub(string.sub(name,5,10),utf8.charpattern,Name.table),2

    elseif Name.DoubleName[string.sub(name,1,2)] then

        -- return Name.DoubleName[string.sub(name,1,2)].." "..string.gsub(string.sub(name,3,10),'(...)',Name.table),1
        return Name.DoubleName[string.sub(name,1,2)].." "..string.gsub(string.sub(name,3,10),utf8.charpattern,Name.table),1

    else

        -- return string.gsub(string.sub(name,1,2),'(...)',Name.table) .." "..string.gsub(string.sub(name,3,10),'(...)',Name.table),0
        return string.gsub(string.sub(name,1,2),utf8.charpattern,Name.table) .." "..string.gsub(string.sub(name,3,10),utf8.charpattern,Name.table),0
    end

    return "",0
end

